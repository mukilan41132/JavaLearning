public class SwitchStatement {

    public static  void main(String[] args){
        char Letter = 'A';
        switch (Letter){
            case 'A':
                System.out.println(Letter+"is able");
                break;
            case 'B':
                System.out.println(Letter+"is ble");
                break;
            case 'C':
                System.out.println(Letter+"is cle");
                break;
        }


    }

}
